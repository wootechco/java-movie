package domain;

public class Ticket {
    public int movieId;
    public int time;
    public int cnt;

    public void setMovieId(int movieId) {
        this.movieId = movieId;
    }

    public void setTime(int time) {
        this.time = time;
    }

    public void setCnt(int cnt) {
        this.cnt = cnt;
    }
}